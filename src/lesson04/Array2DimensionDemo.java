    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int row = 0, col = 0;
        try {
            row = Integer.parseInt(txtRow.getText());
            col = Integer.parseInt(txtCol.getText());
        } catch (NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error occurred!" + e.getMessage());
            return;
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error occurred!" + e.getMessage());
            return;
        }

        int[][] arr = new int[row][col];
        int countChan = 0, countLe = 0, max = 0, min = 0;
        double tbChan = 0, tbLe = 0;
        String arrayAsString = "", result = "";

        Random r = new Random();
        //TODO: tạo 1 mảng 2 chiều random
        for (int i = 0; i < row; i++) {
            for (int j = 0; j < col; j++) {
                arr[i][j] = r.nextInt(100);
                arrayAsString += arr[i][j] + " ";
                if (arr[i][j] % 2 == 0) {
                    countChan++;
                    tbChan += arr[i][j];
                }
                if (arr[i][j] % 2 != 0) {
                    countLe++;
                    tbLe += arr[i][j];
                }
            }
            arrayAsString += "\n";
        }

        //TODO: Thực thi requirement trung bình chẵn/lẻ số phần tử chẵn lẻ
        tbChan = tbChan / countChan;
        tbLe = tbLe / countLe;

        result += "Số phần tử chẵn: " + countChan + "\n";
        result += "Số phần tử lẻ: " + countLe + "\n";
        result += "TB chẵn: " + tbChan + "\n";
        result += "TB lẻ: " + tbLe + "\n";
        result += "Giá trị lớn nhất: " + findMax(arr) + "\n";
        result += "Giá trị nhỏ nhất: " + findMin(arr) + "\n";
        txtArray.setText(arrayAsString);
        txtResult.setText(result);
    }//GEN-LAST:event_btnProcessActionPerformed

